    private void jButtonTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTerminarActionPerformed
        jButtonTerminar.setEnabled(false);
        controlador.setTerminarJuego(true);
        controlador.setHabilitarTablero(false);
        jButtonNuevo.setEnabled(true);
        for (int i = 0; i < controlador.getPosicionFinal().length; i++) {
            this.jPanelTablero.remove(arrayPiezas[i]);
        }
        this.jPanelTablero.revalidate();
        iniciarTablero();
    }//GEN-LAST:event_jButtonTerminarActionPerformed

