    private void iniciarTablero() {
        controlador = new Controlador();
        arrayPiezas = new PiezaRompeCabezas[Constantes.FILAS * Constantes.COLUMNAS];
        int[] posiciones = controlador.getPosicionFinal();
        int i = 0;
        while (i < 9) {
            Icon imagen;
            if (posiciones[i] == -1) {//Si es la primer pieza es la vacia le asigno orden -1 y la imagen vacia
                imagen = new ImageIcon(getClass().getResource("/imagenesPiezas/vacio.jpg"));
            } else {
                imagen = new ImageIcon(getClass().getResource("/imagenesPiezas/" + posiciones[i] + ".jpg"));
            }
            arrayPiezas[i] = new PiezaRompeCabezas(imagen);
            arrayPiezas[i].setOrder(posiciones[i]);
            arrayPiezas[i].addMouseListener(controlador);
            jPanelTablero.setSize(600, 450);
            jPanelTablero.setLayout(new java.awt.GridLayout(Constantes.FILAS, Constantes.COLUMNAS, 0, 0));
            this.jPanelTablero.add(arrayPiezas[i]);
            i++;
        }
    }

